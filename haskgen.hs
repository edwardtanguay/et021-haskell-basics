-- MAIN CODE

main :: IO ()
main = do
    let filePath = "/var/www/haskell/index.html"
    let html = getSitePrecontentHtml 
                ++ getIntroHtml 
                ++ getExampleTwoLists 
                ++ getFooterHtml "0.6" 
                ++ getSitePostcontentHtml
    writeFile filePath html
