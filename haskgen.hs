import System.IO

concatList :: [String] -> String
concatList [] = "" -- Base case
concatList (x:xs) = "<li>" ++ x ++ "</li>" ++ concatList xs

main :: IO ()
main = do
    let filePath = "/var/www/haskell/index.html"
    let nums = ["one", "two", "three", "four", "five"]
    let numList = "<ul>" ++ concatList nums ++ "</ul>"
    let version = "0.3"
    let html = "<h1>Haskell-Generated Site</h1>\n<p>This site was generated by a Haskell script.</p><p>" ++ numList ++ "</p><hr/><p>version: " ++ version  ++ "</p>"
    writeFile filePath html
